ASSUME CS:CODE, DS:DATA
DATA SEGMENT
    M1 DB 10,13, "ENTER FIRST NUMBER: $"
    M2 DB 10,13, "ENTER SECOND NUMBER: $"
    M3 DB 10,13, "PRODUCT: $"
DATA ENDS

PRTMSG MACRO MSG
    LEA DX, MSG
    MOV AH, 09H
    INT 21H
ENDM

GETDCM MACRO
    MOV AH,01H
    INT 21H
    SUB AL,30H
ENDM

CODE SEGMENT
START: MOV AX,DATA
    MOV DS,AX
    PRTMSG M1
    GETDCM
    MOV BL,AL

    PRTMSG M2
    GETDCM

    MOV AH,00H
    MUL BL
    AAM

    MOV BX,AX
    PRTMSG M3

    MOV DL,BH
    ADD DL,30H
    MOV AH,02H
    INT 21H

    MOV DL,BL
    ADD DL,30H
    MOV AH,02H
    INT 21H
    
    MOV AH,4CH
    INT 21H


CODE ENDS
END START